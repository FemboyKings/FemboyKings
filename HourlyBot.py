def hourly():
    while True:
        if datetime.datetime.utcnow().minute == 0 and datetime.datetime.utcnow().second == 0:
            image_url = choice(image_urls)
            bot.send_photo(config.get('channel'), image_url)
            time.sleep(1)
        time.sleep(1)

